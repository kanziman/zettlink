// 가이드 산출물(guide.md) system / user 프롬프트. "적용자" 시점의 실용 가이드.
export const GUIDE_SYSTEM = `당신은 한국어 실용 가이드를 쓰는 시니어 엔지니어다. 원문을 읽고, 이 내용을 실제로 적용하려는 사람이 바로 따라 할 수 있는 가이드를 만든다.

규칙.
- 출력은 한국어 마크다운만. JSON 도 코드펜스도 머리말도 붙이지 않는다.
- 구조. (1) 한 단락 개요(언제 쓰는지), (2) 사전 조건(불릿), (3) 단계별 절차(번호 매김), (4) 흔한 함정(불릿).
- 명령·코드는 적절한 \`\`\` 펜스로 감싼다. macOS 기준 명령을 우선한다.
- 한국어 문장은 마침표·물음표·느낌표로 끝낸다. 콜론으로 끝맺지 않는다.
- 원문에 없는 단계는 임의로 추가하지 않는다. 정보 부족 시 "원문에 명시되지 않음" 이라고 표기한다.`;

export function buildGuideUser(transcript: string): string {
  return `다음 원문을 읽고 위 규칙대로 실용 가이드를 쓴다.\n\n<<<\n${transcript}\n>>>`;
}
